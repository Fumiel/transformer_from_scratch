from dataclasses import dataclass


@dataclass(frozen=True)
class TinyGPTConfig:
    vocab_size: int = 128
    block_size: int = 64
    n_layer: int = 2
    n_head: int = 2
    n_embd: int = 64
    dropout: float = 0.0

    def __post_init__(self) -> None:
        if self.n_embd % self.n_head != 0:
            raise ValueError("n_embd must be divisible by n_head")
